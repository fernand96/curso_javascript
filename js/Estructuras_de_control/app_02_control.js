//COMPARADOR ESTRICTO
const puntaje = 1000;

//DIFERENTE != IGUAL A ==
if(puntaje != 1000){
    console.log("Si es diferente");
}else{
    console.log("No es diferente");
}

//comparador estricto
if(puntaje === "1000"){
    console.log("Si es igual");
}else{
    console.log("No es igual");
}

//si es diferente
if(puntaje !== "1000"){
    console.log("Si es diferente");
}else{
    console.log("No es diferente");
}